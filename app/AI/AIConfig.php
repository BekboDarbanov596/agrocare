    // Можно использовать переменную окружения или константу
    public static function getApiKey(): string
    {
        // Проверяем переменные окружения
        $key = $_ENV['HUGGINGFACE_API_KEY']
            ?? $_ENV['HF_API_KEY']
            ?? getenv('HUGGINGFACE_API_KEY')
            ?? getenv('HF_API_KEY')
            ?? null;

        // Если не найден, читаем напрямую из .env
        if (empty($key) || $key === 'hf_your_api_key_here') {
            $envFile = __DIR__ . '/../../.env';
            if (file_exists($envFile)) {
                $envContent = file_get_contents($envFile);
                // Ищем HUGGINGFACE_API_KEY или HF_API_KEY
                if (preg_match('/HUGGINGFACE_API_KEY\s*=\s*["\']?([^"\'\s\n]+)["\']?/', $envContent, $matches)) {
                    $key = trim($matches[1], " \t\n\r\0\x0B\"'");
                } elseif (preg_match('/HF_API_KEY\s*=\s*["\']?([^"\'\s\n]+)["\']?/', $envContent, $matches)) {
                    $key = trim($matches[1], " \t\n\r\0\x0B\"'");
                }
            }
        }

        // Убираем кавычки если есть
        if ($key) {
            $key = trim($key, '"\'');
        }

        return $key ?: 'hf_your_api_key_here';
    }

